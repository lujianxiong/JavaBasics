package ExtendsRealize;
//第二节 继承的实现： （子类不定义功能实现子类方法调用）
//父类
public class Person {
	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

}
